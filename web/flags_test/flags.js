function randomizeFilter(id){
    var hue = rotateHue();
    var sat = saturateHue();
    var invert = getPercent();
    var sepia = getPercentLow();
    document.getElementById(id).style.filter = 'hue-rotate('+ hue +'deg)';
    document.getElementById(id).style.filter += 'saturate('+ sat +'%)';
    document.getElementById(id).style.filter += 'invert('+ invert + '%)';
    document.getElementById(id).style.filter += 'sepia(' + sepia + '%)';
    console.log('hue-rotate('+ hue +'deg); saturate('+ sat +'%); invert('+ invert + '%); sepia(' + sepia + '%)');
}

